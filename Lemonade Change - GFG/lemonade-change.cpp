class Solution {
  public:
    bool lemonadeChange(int N, vector<int> &bills) {
        // code here
        
        int five=0,ten=0;
        
        for(auto b:bills)
        {
            if(b == 5) five++;
            else if(b == 10) ten++,five--;
            else if(ten>0) ten--, five--;
            else
                five -= 3;
            if(five < 0) return false;
        }
        
        return true;
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;

        vector<int> bills(N);
        for (int i = 0; i < N; i++) cin >> bills[i];

        Solution obj;
        int ans = obj.lemonadeChange(N, bills);
        if (ans)
            cout << "True" << endl;
        else
            cout << "False" << endl;
    }
    return 0;
}
