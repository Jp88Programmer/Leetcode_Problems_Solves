class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        // code here
        set<int>s;
        Node* t1 = head1;
        Node* t2 = head2;
        
        while(t1){
            s.insert(t1->data);
            t1 = t1->next;
        }
        
        while(t2){
            s.insert(t2->data);
            t2 = t2->next;
        }
        
        Node* head = NULL;
        Node* temp = head;
        int count = 0;
        for(auto e : s){
            Node* t = new Node(e);
            if(count == 0){
                head = t;
                count = 1;
                temp = head;
            }else{
                 temp->next = t;
            temp = temp->next;
            }
           
        }
        
        return head;
    }
};


